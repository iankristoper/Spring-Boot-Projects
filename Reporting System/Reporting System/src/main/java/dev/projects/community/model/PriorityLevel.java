public enum PriorityLevel {
    
    LOW,        // Minor issues, no immediate risk (e.g., graffiti, broken bench)
    MEDIUM,     // Needs attention soon (e.g., garbage, potholes)
    HIGH,       // Important, might affect safety or daily life (e.g., power outage, flooding)
    CRITICAL    // Emergency, immediate danger (e.g., fire hazard, major accident)
    
}
